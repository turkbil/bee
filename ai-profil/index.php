        // Search functionality
        function searchSectors() {
            const searchTerm = document.getElementById('sectorSearch').value.toLowerCase();
            const sectors = document.querySelectorAll('.search-result');
            const categories = document.querySelectorAll('.sector-category');
            
            sectors.forEach(sector => {
                const keywords = sector.dataset.keywords || '';
                const text = sector.textContent.toLowerCase();
                
                if (searchTerm === '' || text.includes(searchTerm) || keywords.includes(searchTerm)) {
                    sector.classList.remove('hidden');
                } else {
                    sector.classList.add('hidden');
                }
            });

            // Hide categories with no visible sectors
            categories.forEach(category => {
                const visibleSectors = category.querySelectorAll('.search-result:not(.hidden)');
                if (visibleSectors.length === 0 && searchTerm !== '') {
                    category.style.display = 'none';
                } else {
                    category.style.display = 'block';
                }
            });
        }

        // Checkbox card interactions
