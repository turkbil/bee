       'seo_description.max' => 'SEO a√ßƒ±klamasƒ± en fazla 160 karakter olabilir',
   ];
   
   /**
    * Dil sekmesi deƒüi≈ütir
    */
   public function switchLanguage($language)
   {
       if (in_array($language, $this->availableLanguages)) {
           $this->currentLanguage = $language;
           
           \Log::info('üéØ PageManageComponent switchLanguage √ßaƒürƒ±ldƒ±', [
               'new_language' => $language,
               'current_language' => $this->currentLanguage
           ]);
           
           // JavaScript'e dil deƒüi≈üikliƒüini bildir (TinyMCE i√ßin)
           $this->dispatch('language-switched', [
               'language' => $language,
               'editorId' => "editor_{$language}",
               'content' => $this->multiLangInputs[$language]['body'] ?? ''
           ]);
           
           // SEO Component'e dil deƒüi≈üimini bildir - √áOKLU EVENT DENEMESƒ∞
           $this->dispatch('seo-language-change', ['language' => $language]);
           $this->dispatch('refresh-seo-language', ['language' => $language]);
           
           // Direkt component refresh - alternatif y√∂ntem
           $this->dispatch('$refresh');
           
           \Log::info('‚úÖ PageManageComponent eventleri g√∂nderildi', [
               'language' => $language,
               'events' => ['seo-language-change', 'refresh-seo-language', '$refresh']
           ]);
       }
   }

   public function save($redirect = false, $resetForm = false)
   {
       \Log::info('üöÄ SAVE METHOD BA≈ûLADI!', [
           'pageId' => $this->pageId,
           'redirect' => $redirect,
           'resetForm' => $resetForm,
           'currentLanguage' => $this->currentLanguage
       ]);
      // TinyMCE i√ßeriƒüini senkronize et
      $this->dispatch('sync-tinymce-content');
      
      \Log::info('üîç Validation ba≈ülƒ±yor...', ['currentLanguage' => $this->currentLanguage]);
      
      try {
          $this->validate();
          \Log::info('‚úÖ Validation ba≈üarƒ±lƒ± ge√ßti!');
      } catch (\Exception $e) {
          \Log::error('‚ùå Validation HATASI!', [
              'error' => $e->getMessage(),
              'file' => $e->getFile(),
              'line' => $e->getLine()
          ]);
          
          // Validation hatasƒ± varsa devam etme
          $this->dispatch('toast', [
              'title' => 'Validation Hatasƒ±',
              'message' => $e->getMessage(),
              'type' => 'error'
          ]);
          return;
      }
      
      // JSON formatƒ±nda √ßoklu dil verilerini hazƒ±rla
      $multiLangData = [];
      foreach (['title', 'slug', 'body'] as $field) {
          $multiLangData[$field] = [];
          foreach ($this->availableLanguages as $lang) {
              $value = $this->multiLangInputs[$lang][$field] ?? '';
              
              // Bo≈ü slug'lar i√ßin otomatik olu≈ütur - T√ºrk√ße karakter desteƒüi ile
              if ($field === 'slug' && empty($value) && !empty($this->multiLangInputs[$lang]['title'])) {
                  // Page model'inin custom slug metodunu kullan
                  $page = new \Modules\Page\App\Models\Page();
                  $value = $page->customSlugMethod($this->multiLangInputs[$lang]['title']);
              }
              
              if (!empty($value)) {
                  $multiLangData[$field][$lang] = $value;
              }
          }
      }
      
      // SEO verilerini kaydet - Ayrƒ± field'lardan array olu≈ütur
      $seoData = [
          'title' => $this->seo_title,
          'description' => $this->seo_description,
          'keywords' => $this->seo_keywords,
          'canonical_url' => $this->canonical_url
      ];
      
      \Log::info('üíæ SEO verilerini kaydediliyor...', [
          'seoData' => $seoData,
          'pageId' => $this->pageId,
          'currentLanguage' => $this->currentLanguage,
          'seo_title_length' => strlen($this->seo_title ?? ''),
          'seo_description_length' => strlen($this->seo_description ?? ''),
          'filtered_data' => array_filter($seoData)
      ]);
      
      if ($this->pageId && !empty(array_filter($seoData))) {
          $page = Page::findOrFail($this->pageId);
          
          // HasSeo trait metoduyla SEO verileri kaydet
          if (!empty(array_filter($seoData))) {
              \Log::info('‚úÖ SEO Title hazƒ±rlandƒ±', [
                  'language' => $this->currentLanguage,
                  'title' => $seoData['title']
              ]);
              
              \Log::info('‚úÖ SEO Description hazƒ±rlandƒ±', [
                  'language' => $this->currentLanguage,
                  'description' => substr($seoData['description'], 0, 100) . '...'
              ]);
              
              $page->updateSeoForLanguage($this->currentLanguage, $seoData);
              \Log::info('‚úÖ SEO ayarlarƒ± HasSeo trait ile kaydedildi', [
                  'language' => $this->currentLanguage,
                  'data' => $seoData
              ]);
          }
      }
      
      // SEO Component'e kaydetme event'i g√∂nder
      if ($this->pageId) {
          $this->dispatch('parentFormSaving');
      }
      
      $data = array_merge($this->inputs, $multiLangData);

      // Eƒüer ana sayfa ise pasif yapƒ±lmasƒ±na izin verme
      if (($this->inputs['is_homepage'] || ($this->pageId && Page::find($this->pageId)?->is_homepage)) && isset($data['is_active']) && $data['is_active'] == false) {
          $this->dispatch('toast', [
              'title' => __('admin.warning'),
              'message' => __('page::messages.homepage_cannot_be_deactivated'),
              'type' => 'warning',
          ]);
          return;
      }
   
      if ($this->pageId) {
          $page = Page::findOrFail($this->pageId);
          $currentData = collect($page->toArray())->only(array_keys($data))->all();
          
          // SEO Component'e kaydetme event'i g√∂nder (her durumda)
          $this->dispatch('parentFormSaving');
          
          if ($data == $currentData) {
              // Sayfa deƒüi≈ümemi≈ü ama SEO deƒüi≈ümi≈ü olabilir - her durumda ba≈üarƒ± mesajƒ±
              $toast = [
                  'title' => __('admin.success'),
                  'message' => __('page::messages.page_updated'),
                  'type' => 'success'
              ];
              
              // Page data unchanged, but save successful (SEO may have changed)
          } else {
              $page->update($data);
              log_activity($page, 'g√ºncellendi');
              
              $toast = [
                  'title' => __('admin.success'),
                  'message' => __('page::messages.page_updated'),
                  'type' => 'success'
              ];
          }
      } else {
          $page = Page::create($data);
          $this->pageId = $page->page_id;
          log_activity($page, 'olu≈üturuldu');
          
          // Yeni olu≈üturulan sayfa i√ßin SEO verilerini kaydet
          if (!empty(array_filter($seoData))) {
              \App\Services\SeoFormService::saveSeoData($page, $seoData);
          }
          
          // SEO component verilerini g√ºncelle
          $this->loadSeoComponentData($page);
          
          $toast = [
              'title' => __('admin.success'),
              'message' => __('page::messages.page_created'),
              'type' => 'success'
          ];
      }
   
      \Log::info('üéØ Save method tamamlanƒ±yor...', [
          'pageId' => $this->pageId,
          'redirect' => $redirect,
          'toast' => $toast
      ]);
      
      if ($redirect) {
          session()->flash('toast', $toast);
          return redirect()->route('admin.page.index');
      }
   
      \Log::info('üéä Toast mesajƒ± g√∂nderiliyor...', ['toast' => $toast]);
      $this->dispatch('toast', $toast);
      
      \Log::info('‚úÖ Save method ba≈üarƒ±yla tamamlandƒ±!', ['pageId' => $this->pageId]);
   
      if ($resetForm && !$this->pageId) {
          $this->reset();
          $this->currentLanguage = 'tr';
          // Dinamik olarak aktif dillerden bo≈ü inputs olu≈ütur
          $this->initializeEmptyInputs();
      }
   }

